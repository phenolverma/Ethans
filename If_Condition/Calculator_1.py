x = float(input("enter 1st num "))
y = float(input("enter 2nd num "))
Cal = input("Enter +, -, *, / = ")
if Cal == '+':
    print("Sum of two = ", x + y)
elif Cal == '-':
    print("Sub of two = ", x - y)
elif Cal == '*':
    print("Mul of two = ", x * y)
elif Cal == '/':
    print("Div of two = ", x / y)
print("Success")